
print("Calculadora")

num1 = float(input("Digite um número: "))
num2 = float(input("Digite um número: "))
operacao = input("Digite operacação:")

if operacao == "+" :
    print(f"{num1 + num2}")
if operacao == "-":
    print(f"{num1 - num2}")
if operacao == "*":
    print(f"{num1 * num2}")
if operacao == "/":
    print(f"{num1 / num2}")



     





    

    
